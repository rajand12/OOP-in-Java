//WAP to print first 15 even numbers.

public class FifteenEven{
    public static void main(String[] args) {
        int i,j=0;
        System.out.println("The first 15 even numbers are");
        for(i=0;i<15;i++){
            j=j+2;
            System.out.print(j +" ");
        }
        
    }
}